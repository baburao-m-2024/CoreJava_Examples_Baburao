package basics;

public class DefaultValuesDemo {

	static int id = 90;
	static int rollNo;
	static long mobile;
	static float marks;
	static double aggr;
	static String course;
	static boolean result;
	static char symbol;
	
	public static void main(String[] args) {

		System.out.println(id);
		System.out.println("default value assigned to int type is "+rollNo);
		System.out.println("default value assigned to long type is "+mobile);
		System.out.println("default value assigned to float type is "+ marks);
		System.out.println("default value assigned to double type is "+ aggr);
		System.out.println("default value assigned to String type is "+course);
		System.out.println("default value assigned to boolean type is "+result);
		System.out.println("default value assigned to char type is "+symbol);
		
	}

}
