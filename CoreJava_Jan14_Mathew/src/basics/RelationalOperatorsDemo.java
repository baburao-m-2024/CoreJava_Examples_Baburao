package basics;

public class RelationalOperatorsDemo {

	public static void main(String[] args) {

		int num1 = 15;
		int num2 = 10;
		int num3 = 10;
		
		String course1 = "java";
		String course2 = "java";
		
		System.out.println(num1 > num2);
		System.out.println(num2 < num3);
		System.out.println(num2 >= num3);
		System.out.println(num1 <= num2);
		
		System.out.println(num2 == num3);
		
		System.out.println(course1.equals(course2));
		
	}

}
